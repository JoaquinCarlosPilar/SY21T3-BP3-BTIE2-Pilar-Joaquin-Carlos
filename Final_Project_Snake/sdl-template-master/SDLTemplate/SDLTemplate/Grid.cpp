#include "Grid.h"

/*--------------------------------------------------------------------------------------*/

Grid::Grid()
{

}

/*--------------------------------------------------------------------------------------*/

Grid::~Grid()
{

}

/*--------------------------------------------------------------------------------------*/

void Grid::start()
{
	texture = loadTexture("gfx/grass_background.png");
	width = 20;
	height = 20;
	size = 32;
}

/*--------------------------------------------------------------------------------------*/

void Grid::draw()
{
	for (int i = 0; i < width; i++)
	{
		for (int j = 0; j < height; j++)
		{
			blit(texture, i * size, j * size);
		}
	}
}

/*--------------------------------------------------------------------------------------*/

void Grid::update()
{

}

/*--------------------------------------------------------------------------------------*/

int Grid::getGridWidth()
{
	return width;
}

/*--------------------------------------------------------------------------------------*/

int Grid::getGridHeight()
{
	return height;
}

/*--------------------------------------------------------------------------------------*/

int Grid::getGridSize()
{
	return size;
}