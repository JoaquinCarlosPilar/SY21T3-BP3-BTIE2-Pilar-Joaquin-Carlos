#include "Powerup.h"

/*--------------------------------------------------------------------------------------------------------------------*/

Powerup::Powerup()
{

}

/*--------------------------------------------------------------------------------------------------------------------*/

Powerup::~Powerup()
{

}

/*--------------------------------------------------------------------------------------------------------------------*/

void Powerup::start()
{
	width = 0;
	height = 0;
	speed = 3;
	directionX = 0;
	directionY = 1;

	texture = loadTexture("gfx/Alternate_Points.png");
	SDL_QueryTexture(texture, NULL, NULL, &width, &height);
}

void Powerup::update()
{
	x += directionX * speed;
	y += directionY * speed;
}

/*--------------------------------------------------------------------------------------------------------------------*/

void Powerup::draw()
{
	blit(texture, x, y);
}

/*--------------------------------------------------------------------------------------------------------------------*/

int Powerup::getPositionX()
{
	return x;
}

/*--------------------------------------------------------------------------------------------------------------------*/

int Powerup::getPositionY()
{
	return y;
}

/*--------------------------------------------------------------------------------------------------------------------*/

int Powerup::getWidth()
{
	return width;
}

/*--------------------------------------------------------------------------------------------------------------------*/

int Powerup::getHeight()
{
	return height;
}

/*--------------------------------------------------------------------------------------------------------------------*/

void Powerup::setPosition(int xPos, int yPos)
{
	this->x = xPos;
	this->y = yPos;
}